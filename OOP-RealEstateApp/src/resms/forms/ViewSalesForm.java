    private void displayTransactionsTable() {
        ld = LotDatabase.getInstance();
        td = TransactionDatabase.getInstance(ld);
        ArrayList<TransactionReport> reportList = td.generateReport();
        updateTransactions(reportList);
    }

    private void updateTransactions(List<TransactionReport> reports) {
        TransactionTable transactionModel = new TransactionTable((ArrayList<TransactionReport>) reports);
        this.transactionTable.setModel(transactionModel);
}
    
