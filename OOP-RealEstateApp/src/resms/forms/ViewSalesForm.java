    private void SearchTransactionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchTransactionActionPerformed
        String propertyType = (String) jComboBoxPropertyType.getSelectedItem();
        String action = (String) jComboBoxAction.getSelectedItem();
        String payMethod = (String) jComboBoxPayMethod.getSelectedItem();

        List<TransactionReport> filteredReports = td.generateReport().stream()
            .filter(report -> (propertyType.equals("Any") || report.getLot().getPropertyType().equals(propertyType)))
            .filter(report -> (action.equals("Any") || report.getType().equals(action)))
            .filter(report -> (payMethod.equals("Any") || report.getPayMethod().equals(payMethod)))
            .toList();

        updateTransactions(filteredReports);
    }//GEN-LAST:event_SearchTransactionActionPerformed

    
